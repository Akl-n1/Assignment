namespace Day07_c_
{
    class Calculator
    {
        // 1. Add two integers
        public int Sum(int a, int b) => a + b;

        // 2. Add three integers
        public int Sum(int a, int b, int c) => a + b + c;

        // 3. Add two doubles
        public double Sum(double a, double b) => a + b;
    }
}
